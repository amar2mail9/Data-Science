# This based on condition / decision

# Syntax of if
'''
if condition true:
    block of code execution

'''

# Syntax of if-else
'''
if condition true:
    block of code execution
else:
    block of code execution

'''


# Syntax of if-elif-else
'''
if condition true:
    block of code execution
elif:
    block of code execution
else:
    block of code execution

'''

# Syntax of nested if-else
'''
if condition true:
  
    if condition true:
         // block of code execution
    else: 
         // block of code execution
else:
   if condition true:
         // block of code execution
    else: 
         // block of code execution

'''